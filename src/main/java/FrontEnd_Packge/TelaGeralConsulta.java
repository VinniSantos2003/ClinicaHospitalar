    private void montarTabelaConsulta() {//Funcional
        if (!ConsultaArrayList.ListaDeConsulta.isEmpty()) {
            DefaultTableModel ModeloTabelaConsulta = (DefaultTableModel) this.TabelaConsulta.getModel();
            //Para a tabela de consulta, terei que usar o ID registrado na consulta para achar um objeto que possua 
            //esse id e então retornalo e usar o getNomeCompleto para mostrar o nome do paciente e do médico na tabela

            /*
        Passo 1: pegar o id  do paciente ou médico
        passo 2: olhar objeto por objeto no arraylist que bata com o id paciente ou do medico
        passo 3: retornar o nome do objeto (ou o objeto em si)
       
       
             */
            for (ConsultaMedica c : ConsultaArrayList.ListaDeConsulta) {//Passo 1
                long idMedico = c.getIdMedico();
                long idPaciente = c.getIdPaciente();

                ModeloTabelaConsulta.addRow(
                        new Object[]{
                            getPacienteById(idPaciente).getNomeCompleto(),
                            getMedicoById(idMedico).getNomeCompleto(), /*
                        Colocar mais uma informação no campo de consulta
                        Somente o nome do paciente e do médico não vai ser suficiente
                         */}
                );
            }
        }

    }

