    private void BotaoCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoCadastrarActionPerformed
        // TODO add your handling code here:\
        if (this.ComboBoxTipo.getSelectedItem() == "Médico") {
            //Médico
            if (getNomeCompleto().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Preencha o campo de nome");
            } else if (getRua().isEmpty() || String.valueOf(getNumero()).isEmpty() || String.valueOf(getCep()).isEmpty() || getBairro().isEmpty() || getCidade().isEmpty() || getEstado().isEmpty()) {
                //Verificar se o campo de endereço estão preenchidos
                JOptionPane.showMessageDialog(null, "Preencha todos os campos de endereço");
            } else if (getTelefone().isEmpty() || getCelular().isEmpty() || getEmail().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos de contato pessoal");
            } else if (getDataNascimento() == null) {
                JOptionPane.showMessageDialog(null, "Preencha o campo de data corretamente");
            } else {
                Endereco e = new Endereco(
                        getRua(),
                        getNumero(),
                        getBairro(),
                        getCidade(),
                        getEstado(),
                        getCep());
                ContatoTelEmail c = new ContatoTelEmail(
                        getTelefone(),
                        getCelular(),
                        getEmail());
                Medico m = new Medico(
                        getNomeCompleto(),
                        getDataNascimento(),
                        e,
                        c,
                        getGenero(),
                        getCMR(),
                        getEspecialidades(),
                        getCirurgiao(),
                        getCH(),
                        getSetor());
                MedicoArrayList.ListaDeMedicos.add(m);
                JOptionPane.showMessageDialog(null, "Médico cadastrado com sucesso!");
                limparTela();
            }
        } else {
            if (getNomeCompleto().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Preencha o campo de nome");
            } else if (getRua().isEmpty() || String.valueOf(getNumero()).isEmpty() || String.valueOf(getCep()).isEmpty() || getBairro().isEmpty() || getCidade().isEmpty() || getEstado().isEmpty()) {
                //Verificar se o campo de endereço estão preenchidos
                JOptionPane.showMessageDialog(null, "Preencha todos os campos de endereço");
            } else if (getTelefone().isEmpty() || getCelular().isEmpty() || getEmail().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos de contato pessoal");
            } else if (getDataNascimento() == null) {
                JOptionPane.showMessageDialog(null, "Preencha o campo de data corretamente");
            } else {
                //Enfermeiro
                Endereco e = new Endereco(
                        getRua(),
                        getNumero(),
                        getBairro(),
                        getCidade(),
                        getEstado(),
                        getCep());
                ContatoTelEmail c = new ContatoTelEmail(
                        getTelefone(),
                        getCelular(),
                        getEmail());

                Enfermeiro enf = new Enfermeiro(
                        getNomeCompleto(),
                        getDataNascimento(),
                        e,
                        c,
                        getGenero(),
                        getSetor(),
                        getCH(),
                        getTreinadoRx());
                EnfermeiroArrayList.ListaDeEnfermeiro.add(enf);
                JOptionPane.showMessageDialog(null, "Enfermeiro cadastrado com sucesso!");
                limparTela();
            }
        }
    }//GEN-LAST:event_BotaoCadastrarActionPerformed

