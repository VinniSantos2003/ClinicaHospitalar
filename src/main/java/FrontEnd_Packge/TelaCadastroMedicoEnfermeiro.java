    private void ComboBoxTipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ComboBoxTipoActionPerformed
        // TODO add your handling code here:
        if (this.ComboBoxTipo.getSelectedItem() == "Médico") {
            this.CheckBoxRaioX.setEnabled(false);
            this.CheckBoxCirurgiao.setEnabled(true);
            this.FieldEspecialidades.setEnabled(true);
        } else if (this.ComboBoxTipo.getSelectedItem() == "Enfermeiro") {
            this.CheckBoxRaioX.setEnabled(true);
            this.CheckBoxCirurgiao.setEnabled(false);
            this.FieldEspecialidades.setEnabled(false);
        }
    }//GEN-LAST:event_ComboBoxTipoActionPerformed

